number1 = int(input("enter number1 :"))
ol = input("enter")
number2 = int(input("enter number2 :"))
if ol == "+":
    h = number1 + number2
    print(h)
elif ol == "-":
    a = number1 - number2
    print(a)
elif ol == "*":
    s = number1 * number2
    print(s)
elif ol == "/":
    d = number1 / number2
    print(d)
elif ol == "**":
    g = number1 ** number2
    print(g)
elif ol == "//":
    p = number1 // number2
    print(p)